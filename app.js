//grab the reference
let button=document.querySelector(".get-facts");
let factimage=document.querySelector(".fact-image");
let facts=document.querySelector(".the-facts")
//listening for an event on the button
button.addEventListener("click",pitchjoke)
//fetching response from api and updating in DOM
function pitchjoke(){
fetch(' https://api.chucknorris.io/jokes/random')
.then(res=>res.json())
.then(data=>updateDom(data));
function updateDom(data){
facts.innerHTML=data.value;
factimage.src=data.icon_url;
}
}
//attaching a load event to an image
factimage.addEventListener("load",()=>{
   button.innerText="Here it is"
    })

































































































































































































